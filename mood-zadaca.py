mood = input("What is your mood today? ")


if mood == "happy":
    print("It is great to see you happy!")
elif mood == "nervous":
    print("Take a deep breath 3 times.")
elif mood == "sad":
    print("it will be alright")
elif mood == "excited":
    print("I am happy for you")
elif mood == "relaxed":
    print("ommmm")

else:
    print("I don't recognize this mood")
